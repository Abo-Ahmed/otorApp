
SELECT ORDER.*, 
SHEIKH.PHONE, SHEIKH.CITY, SHEIKH.ADDRESS, SHEIKH.NAME , 
[ORDER_CONTENTS].[CONTENTS] AS CONTENTS 

FROM [ORDER] , [SHEIKH] , [ORDER_CONTENTS]

WHERE ORDER.SHEIKH_NAME = SHEIKH.NAME
AND ORDER.ID = ORDER_CONTENTS.OID
AND ORDER.STATE="POST"

ORDER BY [ORDER].[ID]; 
